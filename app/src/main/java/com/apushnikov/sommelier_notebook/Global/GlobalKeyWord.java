    // TODO: для ускорения разделить на два метода: для первого ключевого слова и для второго
    //==============================================================================
    // Проверяет, есть ли ключевое слово в начале текста
    // Если есть, возращает это ключевое слово
    // Если нет, возвращает ключевое слово
    //==============================================================================
    public static ResultKeyWord thereIsKeyWord(String text) {
        // Создаем возвращемы результат
        ResultKeyWord resultKeyWord;
        resultKeyWord = new ResultKeyWord();
        // Признак того, что ключевое слово найдено
        boolean wordFound = false;
        // Приводим текст к нижнему регистру
        text = text.toLowerCase();
        // Перебираем по строкам - по отдельным ключевым словам
        for (int i = 0; i < KEY_WORDS.length; i++) {
            if (!wordFound) {// Если ключевое слово еще не найдено
                // Перебираем синонимы ключевого слова (перебор начинается со второго слова, т.к.
                // в первом слове - возвращаемое значение
                for (int j = 1; j < KEY_WORDS[i].length; j++) {
                    // Если в начале текста обнаруженое ключевое слова (один из синонимов)
                    if (text.startsWith(KEY_WORDS[i][j])) {
                        // Запоминаем ключевое слово (первый элемент в строке)
                        resultKeyWord.keyWord = KEY_WORDS[i][0];
                        // Запоминаем строки после ключевого слова
                        resultKeyWord.rest = text.substring(KEY_WORDS[i][j].length()).trim();
                        // Если остаток не пуст
                        if (resultKeyWord.rest.length() > 0) {
                            // Делаем первую букву заглавной
                            resultKeyWord.rest = resultKeyWord.rest.substring(0,1).toUpperCase()+ resultKeyWord.rest.substring(1);
                        };
                        // Ключевое слово найдено
                        wordFound = true;
                        break;
                    }
                }
            } else {//Если ключевое слово найдено
                //Выход из цикла
                break;
            }
        }
        return resultKeyWord;
    }

